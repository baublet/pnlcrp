			<h4>The 1948 Canwell UnAmerican Activities Hearings: Photos and documents </h4>
			<p>Shortly after 10:00 a.m. on July  19, 1948, Albert F. Canwell, state representative from Spokane and chair of the Joint Legislative Fact-Finding Committee on Un-American Activities, called the session to order: &ldquo;Before we proceed, I wish to state that we will proceed with proper dignity here; no demonstrations will be tolerated, no speeches from the audience; any violation of that...will be summarily dealt with.&rdquo; So began five days of hearings on the issue of Communist activities at the University of Washington. The Canwell hearings would have wide and lasting consequences. Six tenured faculty members would  face sanctions from the University and three would lose their jobs. Other legislatures and other universities  followed the Washington example and began to look for radicals in the halls of academe. The Cold War Red Scare gained momentum.</p>
			
	
			
