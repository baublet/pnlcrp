			<h4> <em>All Powers Necessary and Convenient: </em>The 1988 Performances of Mark Jenkins' Play </h4>
			
			
	
			
  <p><span class="rightcap" style="width:356px;"><img src="../images/canwell/all%20powers%20flag%20scene.jpg" width="356" height="225">
		 Flag-backed stage in the Playhouse Theater and a scene from "All Powers Necessary and Contingent"</span>    In February 1998, fifty years after the notorious Canwell Committee hearings, Seattle once again watched Albert Canwell  hunt communists at the University of Washington, this time as part of a remarkable play written by Mark Jenkins. <em>All Powers Necessary and Convenient</em> took its title from the enabling legislation that created the Joint Legislative Fact-Finding Committee on Un-American Activities. Produced by the UW School of Drama and featuring a cast of nearly 30 actors, the play used actual testimony to recreate the tense drama of accusation, intimidation, and courage that had unfolded a half century earlier. Large audiences filled the playhouse theater for each performance and attended the lectures and other public events that accompanied the play. And it was quickly apparent  that something unique was happening. <em>All Powers</em> had brought history to life at an important moment, helping several generations come to terms with an important episode in their collective past.</p>
